"""
.2. Escreva um programa que mostre na tela, de 1 até 100, de 1 em 1, 3 vezes.
A primeira vez deve usar a estrutura de repetição for, a segunda while, e a
terceira while.
"""
print('Usando for')
for n in range(1, 100):
    print(n)

print('Usando WHILE')
i = 1
while i <= 100:
    print(i)
    i = i + 1
